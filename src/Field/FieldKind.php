/**
 * Resolves vtiger_field.field_kind from legacy metadata (uitype, fieldname, typeofdata).
 * Used by migrations/backfill; runtime reads field_kind from DB once wired (Phase 1b).
 */
final class FieldKind
{
	/** @var array<int, string> */
	private const UITYPE_MAP = [
		3   => 'autogenerated',
		4   => 'recordNumber',
