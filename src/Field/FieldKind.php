	/** @var array<int, string> uitype → field_kind when not in UITYPE_MAP and not in vtiger_ws_fieldtype */
	private const UITYPE_FALLBACK = [
		1   => 'string',
		2   => 'string',
		5   => 'date',
		6   => 'date',
		14  => 'time',
		19  => 'text',
		20  => 'text',
		21  => 'text',
		22  => 'text',
		24  => 'text',
		70  => 'datetime',
		85  => 'string',
		255 => 'string',
		300 => 'text',
	];

	/**
	 * @param array<int, string> $webserviceByUitype uitype → vtiger_ws_fieldtype.fieldtype
	 */
	public static function resolve(int $uitype, string $fieldName, string $typeofdata, array $webserviceByUitype): string
	{
		if ($uitype === 55) {
			if ($fieldName === 'salutationtype') {
				return 'picklist';
			}
			if ($fieldName === 'firstname') {
				return 'salutation';
			}
		}

		if (isset(self::UITYPE_MAP[$uitype])) {
			return self::UITYPE_MAP[$uitype];
		}

		if (isset($webserviceByUitype[$uitype])) {
			return $webserviceByUitype[$uitype];
		}

		if (isset(self::UITYPE_FALLBACK[$uitype])) {
			return self::UITYPE_FALLBACK[$uitype];
		}

		return self::fromTypeofdata($typeofdata, $uitype);
	}

	private static function fromTypeofdata(string $typeofdata, int $uitype): string
	{
		$code = FieldDefinition::normalizeTypeofdata($typeofdata);

		return match ($code) {
			'V'     => 'string',
			'M'     => 'text',
			'D'     => 'date',
			'DT'    => 'datetime',
			'T'     => 'time',
			'I'     => 'integer',
			'N'     => in_array($uitype, [71, 72], true) ? 'currency' : 'decimal',
			'NN'    => 'float',
			'C'     => 'boolean',
			'E'     => 'email',
			'P'     => 'password',
			default => throw new \InvalidArgumentException(
				"Cannot resolve field_kind for uitype {$uitype} with typeofdata '{$typeofdata}'"
			),
		};
	}
}
